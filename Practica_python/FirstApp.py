# Определяем функцию для сортировки по возрастанию методом выбора
def selection_sort_asc(arr):
    # Сортировка по возрастанию с использованием алгоритма сортировки выбором
    n = len(arr) # Записываем в переменную n длину массива для использования в циклах
    # Внешний цикл проходит по всем элементам кроме последнего
    for i in range(n-1): 
        # Предполагаем, что текущий элемент i - минимальный
        min_idx = i
        # Внутренний цикл проходит по всем элементам справа от текущего
        for j in range(i+1, n):
            # Если находим элемент меньше текущего минимального
            if arr[j] < arr[min_idx]:
                # Обновляем индекс минимального элемента
                min_idx = j
        # Меняем местами текущий элемент i с найденным минимальным элементом
        arr[i], arr[min_idx] = arr[min_idx], arr[i]
    # Возвращаем отсортированный массив
    return arr


# Определяем функцию для сортировки по убыванию методом выбора
